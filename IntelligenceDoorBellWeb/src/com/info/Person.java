package com.info;


public class Person  {
      
	private String tel;
	private String yanzhengma;
	private String nickname;
	private String password;
	private String confirmpassword;
	
	public Person(){
		
	}
	
	public 	Person(String tel,String yanzhengma,String nickname,String password,String confirmpassword){
		super();
		this.tel = tel;
		this.yanzhengma = yanzhengma;
		this.nickname = nickname;
		this.password = password;
		this.confirmpassword = confirmpassword;
	}

	public String getTel() {
		return tel;
	}

	public void setTel(String tel) {
		this.tel = tel;
	}

	public String getYanzhengma() {
		return yanzhengma;
	}

	public void setYanzhengma(String yanzhengma) {
		this.yanzhengma = yanzhengma;
	}

	public String getNickname() {
		return nickname;
	}

	public void setNickname(String nickname) {
		this.nickname = nickname;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	public String getConfirmpassword() {
		return confirmpassword;
	}

	public void setConfirmpassword(String confirmpassword) {
		this.confirmpassword = confirmpassword;
	}
	
	public String toString(){
		return "person [tel="+tel+",yanzheng="+yanzhengma+",nickname="+nickname+",password="+password+",confirmpassword="+confirmpassword+"]";
	}
	
	
	
}
